let grade = prompt("Enter your mark");

if (grade >=80 || grade <=100) {
    console.log("A grade");}
else if (grade >=70 || grade >=79){
    console.log("B grade");}

else if (grade >=60 || grade >= 69){
    console.log("C grade");}

else if (grade >=50 || grade >= 59){
    console.log("D grade");}
else {
    console.log("F grade");}